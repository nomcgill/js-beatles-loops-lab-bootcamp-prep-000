function theBeatlesPlay(musicians, instruments){
 musicians = ["John Lennon", "Paul McCartney", "George Harrison", "Ringo Starr"];
 instruments = ["Guitar", "Bass Guitar", "Lead Guitar", "Drums"];
 var plays = " plays ";

  for (let i = 0; i < 4; i++)
  { 
    return musicians[i] + plays + instruments[i];
  }
}